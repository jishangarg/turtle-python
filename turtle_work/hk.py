import turtle
import math
t=turtle.Turtle()
bgnd=turtle.Screen()
bgnd.title('Hare Krishna')
turtle.colormode(255)
bgnd.bgcolor('red')
t.speed(3)
t.shape('turtle')
t.pensize(3)
t.color('blue','green')

def H():
    t.down()
    t.lt(90)
    t.fd(50)
    t.up()
    t.bk(25)
    t.rt(90)
    t.down()
    t.fd(25)
    t.rt(90)
    t.fd(25)
    t.bk(50)
    t.fd(50)
    t.lt(90)
    t.up()
    t.fd(25)

def A():
    t.down()
    t.lt(80)
    t.fd(50)
    t.rt(160)
    t.fd(50)
    t.bk(25)
    t.rt(100)
    t.fd(10)
    t.up()
    t.bk(10)
    t.lt(100)
    t.fd(25)
    t.lt(80)
    t.fd(25)

def R():
    t.lt(90)
    t.down()
    t.fd(50)
    t.rt(90)
    t.fd(25)
    t.rt(90)
    t.fd(20)
    t.rt(90)
    t.fd(25)
    t.lt(135)
    t.fd(30*1.414)
    t.lt(45)
    t.up()
    t.fd(25)

def E():
    t.down()
    t.fd(25)
    t.bk(25)
    t.lt(90)
    t.fd(25)
    t.rt(90)
    t.fd(25)
    t.bk(25)
    t.lt(90)
    t.fd(25)
    t.rt(90)
    t.fd(25)
    t.bk(25)
    t.rt(90)
    t.fd(50)
    t.lt(90)
    t.up()
    t.fd(50)

def K():
    t.down()
    t.lt(90)
    t.fd(25)
    t.rt(135)
    t.fd(25*1.414)
    t.bk(25*1.414)
    t.lt(90)
    t.fd(25*1.414)
    t.bk(25*1.414)
    t.lt(45)
    t.fd(25)
    t.bk(50)
    t.rt(90)
    t.up()
    t.fd(50)

def I():

    t.lt(90)
    t.down()
    t.fd(50)
    t.bk(50)
    t.rt(90)
    t.up()
    t.fd(25)

def S():
    t.down()
    t.fd(25)
    t.lt(90)
    t.fd(25)
    t.lt(90)
    t.fd(25)
    t.rt(90)
    t.fd(25)
    t.rt(90)
    t.fd(25)
    t.lt(90)
    t.up()
    t.bk(50)
    t.rt(90)
    t.fd(25)

def N():
    t.lt(90)
    t.down()
    t.fd(50)
    t.rt(150)
    t.fd(45/(math.sin(math.degrees(60))))
    t.lt(150)
    t.fd(50)
    t.bk(50)
    t.rt(90)
    t.up()
    t.fd(25)

def pattern():
    t.down()
    list1=['yellow','blue','red','green']
    bg=turtle.Screen()
    t.color('yellow')
    t.speed(0)
    t.pensize(2)
    for i in range(72):
        t.color(list1[i%len(list1)])
        t.circle(25,steps=4)
        t.left(5)
    t.up()

#HARE KRISHNA
t.up()
t.goto(-200,100)
H()
A()
R()
E()
t.goto(-200,0)
K()
R()
I()
S()
H()
N()
A()

t.goto(-300,250)
pattern()
t.goto(-300,-100)
pattern()
t.goto(200,-100)
pattern()
t.goto(200,250)
pattern()

#face
t.speed(1)
t.color('black')
t.up()
t.goto(-50,-200)
t.down()
t.begin_fill()
t.circle(75)
t.end_fill()
t.up()
#eyes
t.goto(-85,-110)
t.down()
t.color('white')
t.begin_fill()
t.fillcolor('white')
t.circle(10)
t.end_fill()
t.up()
t.goto(-15,-110)
t.down()
t.begin_fill()
t.fillcolor('white')
t.circle(10)
t.end_fill()
t.up()
#lips
t.goto(-80,-150)
t.down()
t.speed(6)
t.rt(90)
for i in range(90):
    t.forward(1)
    t.lt(2)

#mor pankh
t.up()
t.goto(-35,-100)
t.color('black')
t.down()
t.speed(0)
t.pensize(1)
for i in range(50):
    t.fd(2)
    t.rt(90)
    t.fd(5)
    t.bk(5)
    t.lt(180)
    t.fd(5)
    t.bk(5)
    t.rt(90)
    t.rt(2)

t.hideturtle()
turtle.done()